import SwiftUI

extension Color {
    static let viewBackground = Color(uiColor: .viewBackground)     // view 배경 색상
    static let imageBackground = Color(uiColor: .imageBackground)   // 행담이 image view 배경
    static let cellBackground = Color(uiColor: .cellBackground)     // 행담이 컬렉션 cell 배경
    static let tabBackground = Color(uiColor: .tabBackground)       // 탭바 배경, 행담이 상태 블록 배경
    static let buttonBackground = Color(uiColor: .buttonBackground) // 행복 작성하기 button 배경
    static let textAccent = Color(uiColor: .textAccent)             // 제일 진한 갈색 (탭 하이라이트, 타이틀 글자색)
}
